package com.alg.scholarship.bean;

public class StudentRole {
	private Integer student_sno;
	private String student_name;
	private String student_major;
	private String student_achievement;
	private String  student_ranking;
	private String  student_grade;
	public Integer getStudent_sno() {
		return student_sno;
	}
	public void setStudent_sno(Integer student_sno) {
		this.student_sno = student_sno;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getStudent_major() {
		return student_major;
	}
	public void setStudent_major(String student_major) {
		this.student_major = student_major;
	}
	public String getStudent_achievement() {
		return student_achievement;
	}
	public void setStudent_achievement(String student_achievement) {
		this.student_achievement = student_achievement;
	}
	public String getStudent_ranking() {
		return student_ranking;
	}
	public void setStudent_ranking(String student_ranking) {
		this.student_ranking = student_ranking;
	}
	public String getStudent_grade() {
		return student_grade;
	}
	public void setStudent_grade(String student_grade) {
		this.student_grade = student_grade;
	}
	
	

}
