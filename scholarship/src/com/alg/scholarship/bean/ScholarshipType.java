package com.alg.scholarship.bean;

public class ScholarshipType {
	private Integer scholarship_id;
	private String scholarship_name;
	private Integer scholarship_type;
	private String scholarship_brief;
	private String  scholarship_remarks;
	public Integer getScholarship_id() {
		return scholarship_id;
	}
	public void setScholarship_id(Integer scholarship_id) {
		this.scholarship_id = scholarship_id;
	}
	public String getScholarship_name() {
		return scholarship_name;
	}
	public void setScholarship_name(String scholarship_name) {
		this.scholarship_name = scholarship_name;
	}
	public Integer getScholarship_type() {
		return scholarship_type;
	}
	public void setScholarship_type(Integer scholarship_type) {
		this.scholarship_type = scholarship_type;
	}
	public String getScholarship_brief() {
		return scholarship_brief;
	}
	public void setScholarship_brief(String scholarship_brief) {
		this.scholarship_brief = scholarship_brief;
	}
	public String getScholarship_remarks() {
		return scholarship_remarks;
	}
	public void setScholarship_remarks(String scholarship_remarks) {
		this.scholarship_remarks = scholarship_remarks;
	}
	
	
}

