package com.alg.scholarship.bean;

public class Announcement {
	private Integer ann_id;
	private String ann_tile;
	private String ann_content;
	private String ann_date;
	private Integer ann_state;
	public Integer getAnn_id() {
		return ann_id;
	}
	public void setAnn_id(Integer ann_id) {
		this.ann_id = ann_id;
	}
	public String getAnn_tile() {
		return ann_tile;
	}
	public void setAnn_tile(String ann_tile) {
		this.ann_tile = ann_tile;
	}
	public String getAnn_content() {
		return ann_content;
	}
	public void setAnn_content(String ann_content) {
		this.ann_content = ann_content;
	}
	public String getAnn_date() {
		return ann_date;
	}
	public void setAnn_date(String ann_date) {
		this.ann_date = ann_date;
	}
	public Integer getAnn_state() {
		return ann_state;
	}
	public void setAnn_state(Integer ann_state) {
		this.ann_state = ann_state;
	}

	
}
